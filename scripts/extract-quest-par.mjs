/**
 * One-shot: read slot_PAR xlsx → generated TS for Quest Raider.
 * Run: node scripts/extract-quest-par.mjs [path-to.xlsx]
 */
import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';
import XLSX from 'xlsx';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const defaultXlsx = path.join(
  process.env.HOME ?? '',
  'Desktop',
  'slot_PAR_file_v3_target94.xlsx',
);
const xlsxPath = process.argv[2] || defaultXlsx;

if (!fs.existsSync(xlsxPath)) {
  console.error('Missing xlsx:', xlsxPath);
  process.exit(1);
}

const wb = XLSX.readFile(xlsxPath, { cellDates: false });

function sheetRows(name) {
  const sh = wb.Sheets[name];
  if (!sh) throw new Error(`Sheet not found: ${name}`);
  return XLSX.utils.sheet_to_json(sh, { header: 1, defval: '' });
}

const reelRows = sheetRows('Reel_Strips');
/** Row 0 title, row 1 blank, row 2 headers — data from row index 3 (Stop # 1) */
const strip = [[], [], [], [], []];
for (let i = 3; i < reelRows.length; i++) {
  const row = reelRows[i];
  const stop = row[0];
  if (stop === '' || stop === undefined) continue;
  for (let c = 0; c < 5; c++) {
    const v = row[1 + c];
    const code = String(v ?? '').trim();
    if (!code) break;
    strip[c].push(code);
  }
}

if (strip.some((s) => s.length === 0)) {
  throw new Error('Failed to parse reel strips');
}

const payRows = sheetRows('Paytable');
const payByCode = {};
for (let i = 3; i < payRows.length; i++) {
  const row = payRows[i];
  const sym = String(row[0] ?? '').trim();
  if (!sym || sym === 'Pay Rule Summary') break;
  payByCode[sym] = {
    three: Number(row[1]) || 0,
    four: Number(row[2]) || 0,
    five: Number(row[3]) || 0,
    minMatch: Number(row[4]) || 3,
  };
}

const outDir = path.join(root, 'src/games/questraider/math');
fs.mkdirSync(outDir, { recursive: true });

const outFile = path.join(outDir, 'parGenerated.ts');
const content = `/* eslint-disable */
// AUTO-GENERATED by scripts/extract-quest-par.mjs — do not edit by hand.
export const PAR_SOURCE_FILE = ${JSON.stringify(path.basename(xlsxPath))};

/** Per-reel strip symbol codes as in PAR (A = wild, 2–8 = paying tiers). */
export const PAR_REEL_STRIP_CODES: readonly string[][] = ${JSON.stringify(strip)};

export const PAR_PAYTABLE_BY_CODE: Record<
  string,
  { three: number; four: number; five: number; minMatch: number }
> = ${JSON.stringify(payByCode, null, 2)};
`;

fs.writeFileSync(outFile, content, 'utf8');
console.log('Wrote', outFile);
console.log('Reel lengths:', strip.map((s) => s.length).join(', '));
